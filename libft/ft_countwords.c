#include "libft.h"

int			ft_countwords(char const *s, char c)
{
	unsigned int		i;

	i = 0;
	if (*s != c && *s != 0)
	{
		i++;
		s++;
	}
	while (*s != 0)
	{
		if (*s != c && s[-1] == c)
			i++;
		s++;
	}
	return (i);
}
